console.log(`\nPrinting the boolean value: ${true}\n`);
console.log(`Printing the boolean value: ${false}\n`);
console.log(`Printing the boolean value with bang in front: ${!true}\n`);
console.log(`Printing the boolean value with bang in front: ${!false}\n`);

/*

Note:

-------------------------------------------------------------------------------

! - negation
&& - AND, true only when both sides evaluate to true
|| - OR, false only when both sides evaluate to false
&& has a higher presidence unless you force another order with parantheses

*/