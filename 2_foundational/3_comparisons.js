console.log(`\nEvaluate 42 === 42: ${42 === 42}\n`);
console.log(`Evaluate true !== true: ${true !== true}\n`);
console.log(`Evaluate true !== 'true': ${true !== 'true'}\n`);

console.log(`Evaluate 'cat' > 'dog': ${'cat' > 'dog'}. Why? Because they're compared alphabetically\n`);

/* 

Note:

! - negation
== - equality, won't type check (compares values only)
=== - equality, will type check (strictly equal, compares values and types) 
< - less than
> - greater than
<= - less than or equal to
>= - greather than or equal to

Strings are compared alphabetically.

*/